package org.mule.runtime.tracer.api.span;

public class StringSpanAttribute implements org.mule.runtime.tracer.api.span.SpanAttribute<String> {

  private final String key;
  private final String value;

  public StringSpanAttribute(String key, String value) {
    this.key = key;
    this.value = value;
  }

  @Override
  public String getKey() {
    return key;
  }

  @Override
  public String getValue() {
    return value;
  }
}
