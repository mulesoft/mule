package org.mule.extension.db.internal.result.statement;

import org.mule.extension.db.internal.domain.autogeneratedkey.AutoGeneratedKeyStrategy;
import org.mule.extension.db.internal.domain.connection.DbConnection;
import org.mule.extension.db.internal.domain.query.QueryTemplate;
import org.mule.extension.db.internal.result.resultset.ResultSetHandler;

import java.sql.ResultSet;
import java.sql.SQLException;
import java.sql.Statement;
import java.util.HashMap;
import java.util.Map;

/**
 * Processes {@link Statement} eagerly fetching all results from any returned {@link ResultSet} and then closes the statement.
 */
public class EagerStatementResultHandler extends AbstractMapStatementResultHandler {

  public EagerStatementResultHandler(ResultSetHandler resultSetHandler) {
    super(resultSetHandler);
  }

  @Override
  public Map<String, Object> processStatement(DbConnection connection, Statement statement, QueryTemplate queryTemplate,
                                              AutoGeneratedKeyStrategy autoGeneratedKeyStrategy)
      throws SQLException {
    try {
      return super.processStatement(connection, statement, queryTemplate, autoGeneratedKeyStrategy);
    } finally {
      statement.close();
    }
  }

  @Override
  protected Map<String, Object> createResultMap() {
    return new HashMap<>();
  }
}
